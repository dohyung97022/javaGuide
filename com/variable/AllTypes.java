package com.variable;


public class AllTypes {
    public static void main(String[] args) {
        // 기본형 8개 (boolean, char, byte, short, int, long, float, double)
        // 참조형 (String, System 등)

        // 실수형
        byte byteType = 127;
        short shortType = 30000;
        int intType = 2000000000;
        long longType = 10*18L;

        // 상수형
        float floatType = 1.11f;
        // float의 정밀도는 7이다.
        double doubleType = 1.11;
        // double의 정밀도는 15이다.

        // 문자형
        char charType = 'a';
        String stringType = "abc";
        stringType = "";

        // 논리형
        boolean boolType = true;
    }
}
