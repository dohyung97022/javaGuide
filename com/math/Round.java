package com.math;

public class Round {
    public static void main(String[] args) {
        // 반올림
        double pi = 3.841592;
        System.out.println(Math.round(pi));

        // 절대값
        System.out.println(Math.abs(pi));

        // 내림
        System.out.println(Math.floor(pi));

        // 올림
        System.out.println(Math.ceil(pi));
    }
}
